#include <stdio.h>

int main() {
    int a = 10;          // Kieu int: So nguyên, ví du 10
    float b = 3.14;      // Kieu float: So thuc, ví du 3.14
    char c = 'A';        // Kieu char: Ký tu, ví du 'A'
    double d = 2.71828;  // Kieu double: So thuc chính xác cao hon float
    unsigned int e = 50; // Kieu unsigned int: So nguyên không âm, ví du 50
    return 0;
}
